public class PointGeo {
    private double lat;
    private double lng;

    public PointGeo(double lat, double lng) {
        this.lat = lat;
        this.lng = lng;
    }

    public double getLat() {
        return lat;
    }

    public double getLng() {
        return lng;
    }
    
    
}
